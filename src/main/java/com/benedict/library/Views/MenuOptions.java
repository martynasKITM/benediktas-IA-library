package com.benedict.library.Views;

/**
 * Enum representing the different menu options available in the application.
 * These options correspond to different views that the user can navigate to.
 */
public enum MenuOptions {
    /**
     * Represents the view where authors are listed.
     */
    AUTHORS,

    /**
     * Represents the view where a new author can be created.
     */
    CREATE_AUTHOR,

    /**
     * Represents the view where income information is displayed.
     */
    INCOME,

    /**
     * Represents the view where new income entries can be added.
     */
    CREATE_INCOME
}
